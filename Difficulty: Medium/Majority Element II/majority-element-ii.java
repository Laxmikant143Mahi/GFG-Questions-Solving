class Solution {
    public List<Integer> findMajority(int[] nums) {
        List<Integer> result = new ArrayList<>();
        if (nums.length == 0) return result;

        // Initialize candidate1, candidate2, and their respective counts
        int candidate1 = 0, candidate2 = 0, count1 = 0, count2 = 0;

        // First pass to find two potential candidates
        for (int num : nums) {
            if (num == candidate1) {
                count1++;
            } else if (num == candidate2) {
                count2++;
            } else if (count1 == 0) {
                candidate1 = num;
                count1 = 1;
            } else if (count2 == 0) {
                candidate2 = num;
                count2 = 1;
            } else {
                count1--;
                count2--;
            }
        }

        // Second pass to verify the counts of candidate1 and candidate2
        count1 = 0;
        count2 = 0;
        for (int num : nums) {
            if (num == candidate1) {
                count1++;
            } else if (num == candidate2) {
                count2++;
            }
        }

        // Add the candidates to the result if their counts exceed n/3
        if (count1 > nums.length / 3) result.add(candidate1);
        if (count2 > nums.length / 3) result.add(candidate2);

        // Sort the result to ensure it is in ascending order
        Collections.sort(result);

        return result;
    }
}
